    public void atualizarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaVendas.getModel();
        modelo.setNumRows(0);
        Venda vendaF = new Venda();
        for (Iterator<Venda> it = vendaF.tabelaVendas().iterator(); it.hasNext();) {            
            Venda venda = (Venda) it.next();
            modelo.addRow(new Object[]{
                venda.getIdVenda(),
                venda.getCpfFuncionario(),                
                venda.getCpfCliente(),
                venda.getDataVenda(),
                venda.getHoraVenda(),  
                venda.getValorVenda(),
                venda.getDscArma(),   
                
            });  
        }
        } 

